import { Card, CardContent } from "@/components/ui/Card";

interface InterpretationData {
  enabled: boolean;
  model: string;
  summary: string;
  mechanisms: string;
  clinical_relevance: string;
  limitations: string;
  full_text: string;
  error: string | null;
  input_tokens?: number;
  output_tokens?: number;
}

interface InterpretationTabProps {
  data: InterpretationData;
}

export default function InterpretationTab({ data }: InterpretationTabProps) {
  if (!data.enabled) {
    return (
      <Card>
        <CardContent className="py-10 text-center space-y-3">
          <div className="text-4xl">🤖</div>
          <p className="text-base font-medium text-gray-700">
            LLM Interpretation is disabled
          </p>
          <p className="text-sm text-gray-500 max-w-md mx-auto">
            To enable AI-powered scientific interpretation, set{" "}
            <code className="bg-gray-100 px-1 rounded text-xs">ANTHROPIC_API_KEY</code> and{" "}
            <code className="bg-gray-100 px-1 rounded text-xs">LLM_ENABLED=true</code>{" "}
            in your backend environment.
          </p>
        </CardContent>
      </Card>
    );
  }

  if (data.error) {
    return (
      <Card>
        <CardContent className="py-8 text-center space-y-2">
          <p className="text-sm font-medium text-red-600">Interpretation failed</p>
          <p className="text-xs text-gray-500 font-mono max-w-lg mx-auto">{data.error}</p>
        </CardContent>
      </Card>
    );
  }

  if (!data.full_text) {
    return (
      <Card>
        <CardContent className="py-8 text-center text-gray-400 text-sm">
          No interpretation generated.
        </CardContent>
      </Card>
    );
  }

  return (
    <div className="space-y-4">
      {/* Header */}
      <div className="flex items-center justify-between">
        <div>
          <h2 className="text-base font-semibold text-gray-900">
            AI Scientific Interpretation
          </h2>
          <p className="text-xs text-gray-400">
            Generated by {data.model} · Review before publication
          </p>
        </div>
        {data.input_tokens != null && (
          <span className="text-xs text-gray-400 font-mono">
            {data.input_tokens + (data.output_tokens ?? 0)} tokens
          </span>
        )}
      </div>

      {/* Disclaimer banner */}
      <div className="rounded-lg border border-amber-200 bg-amber-50 px-4 py-3 text-xs text-amber-800">
        <strong>Research use only.</strong> This AI interpretation is provided as a starting point
        for scientific discussion. Validate all statements against primary literature before
        including in publications.
      </div>

      {/* Sections */}
      {data.summary && (
        <InterpretationSection
          title="Executive Summary"
          icon="📋"
          color="blue"
          content={data.summary}
        />
      )}

      {data.mechanisms && (
        <InterpretationSection
          title="Mechanistic Insights"
          icon="🔬"
          color="purple"
          content={data.mechanisms}
        />
      )}

      {data.clinical_relevance && (
        <InterpretationSection
          title="Clinical Relevance"
          icon="🏥"
          color="green"
          content={data.clinical_relevance}
        />
      )}

      {data.limitations && (
        <InterpretationSection
          title="Limitations & Caveats"
          icon="⚠️"
          color="amber"
          content={data.limitations}
        />
      )}

      {/* Full text collapsible */}
      <FullTextSection text={data.full_text} />
    </div>
  );
}

function InterpretationSection({
  title,
  icon,
  color,
  content,
}: {
  title: string;
  icon: string;
  color: "blue" | "purple" | "green" | "amber";
  content: string;
}) {
  const colorMap = {
    blue: "border-blue-200 bg-blue-50",
    purple: "border-purple-200 bg-purple-50",
    green: "border-green-200 bg-green-50",
    amber: "border-amber-200 bg-amber-50",
  };
  const titleColorMap = {
    blue: "text-blue-800",
    purple: "text-purple-800",
    green: "text-green-800",
    amber: "text-amber-800",
  };

  // Split content into paragraphs / bullet points
  const paragraphs = content
    .split(/\n+/)
    .map((p) => p.trim())
    .filter(Boolean);

  return (
    <Card className={`border ${colorMap[color]}`}>
      <CardContent className="py-4 px-5">
        <h3 className={`text-sm font-semibold mb-3 flex items-center gap-2 ${titleColorMap[color]}`}>
          <span>{icon}</span>
          {title}
        </h3>
        <div className="space-y-2">
          {paragraphs.map((para, i) => {
            const isBullet = para.startsWith("•") || para.startsWith("-") || /^\d+\./.test(para);
            return (
              <p
                key={i}
                className={`text-sm text-gray-700 leading-relaxed ${
                  isBullet ? "pl-3 border-l-2 border-gray-200" : ""
                }`}
              >
                {para}
              </p>
            );
          })}
        </div>
      </CardContent>
    </Card>
  );
}

function FullTextSection({ text }: { text: string }) {
  return (
    <details className="group">
      <summary className="cursor-pointer text-xs text-gray-400 hover:text-gray-600 select-none list-none flex items-center gap-1">
        <span className="group-open:rotate-90 transition-transform inline-block">▶</span>
        View raw LLM output
      </summary>
      <Card className="mt-2">
        <CardContent className="py-3">
          <pre className="text-xs font-mono text-gray-600 whitespace-pre-wrap leading-relaxed">
            {text}
          </pre>
        </CardContent>
      </Card>
    </details>
  );
}
